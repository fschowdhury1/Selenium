package loop;

public class Forloop {

public static void main(String[] args) {
	
//For LOOP	
for(int i=1;i<=10;i++){	
System.out.println(1);	
}	
	
//while LOOP	
int i=1;	
while(i<12){	
System.out.println("BD");	
i++;	
}	


///DO While LOOP	
int y=1;	
do {	
System.out.println("USA");	
y=y+1;	
} while(y<=10);

	
//For each LOOP	
int[]car= {10,20,30,40};	
for(int eachName:car) {	
System.out.println(eachName);	
}





	}

}
